SemaphoreHandle_t transfer_completed;
QueueHandle_t adc_avg_values;

typedef struct {
	uint32_t chn1;
	uint32_t chn2;
}ADC_CHANNELS;

#define ADC_ARRAY_LENGTH 10
#define THRESHOLD_VALUE 2048

uint32_t adc_data[ADC_ARRAY_LENGTH]; // Add adc_data to live expressions to see how the buffer is filled


static void Adc(void *pvParameters){

	ADC_CHANNELS adc;
	uint32_t average, sum;

	// Starts ADC with DMA
	HAL_ADC_Start_DMA(&hadc1, adc_data, ADC_ARRAY_LENGTH);

	while (1){

		// Wait for DMA transfer to be completed
		xSemaphoreTake(transfer_completed, portMAX_DELAY);

		// Calculate the average for each channel
		adc.chn1 = 0;
		adc.chn2 = 0;

		for (uint32_t i=0; i<ADC_ARRAY_LENGTH; i++){

			if(i%2){
				adc.chn2 += adc_data[i];
			}
			else{
				adc.chn1 += adc_data[i];
			}

		}

		adc.chn1 = adc.chn1 / (ADC_ARRAY_LENGTH/2);
		adc.chn2 = adc.chn2 / (ADC_ARRAY_LENGTH/2);

		xQueueSend(adc_avg_values, &adc, portMAX_DELAY);

	}

}

static void Led(void *pvParameters){

	ADC_CHANNELS adc;

	while (1){

		xQueueReceive(adc_avg_values, &adc, portMAX_DELAY);

		// Led 1
		if ( adc.chn1 > THRESHOLD_VALUE ) {

			// If the value coming from the queue is greater than the threshold, turn on the led
			HAL_GPIO_WritePin(LED_1_GPIO_Port, LED_1_Pin, GPIO_PIN_RESET);

		}
		else {

			// Else turn it off
			HAL_GPIO_WritePin(LED_1_GPIO_Port, LED_1_Pin, GPIO_PIN_SET);

		}

		// Led 2
		if ( adc.chn2 > THRESHOLD_VALUE ) {
			HAL_GPIO_WritePin(LED_2_GPIO_Port, LED_2_Pin, GPIO_PIN_RESET);

		}
		else {
			HAL_GPIO_WritePin(LED_2_GPIO_Port, LED_2_Pin, GPIO_PIN_SET);

		}

	}
}


// Called when the buffer was filled
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc){

	static portBASE_TYPE xHigherPriorityTaskWoken = pdFALSE;

	// Release the semaphore, indicating that the DMA transfer was completed
	xSemaphoreGiveFromISR(transfer_completed, xHigherPriorityTaskWoken);

	portEND_SWITCHING_ISR(xHigherPriorityTaskWoken);
}

